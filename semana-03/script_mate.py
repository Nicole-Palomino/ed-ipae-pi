import matematica.calculadora

a = 50
b = 30

print("{} + {} = {}".format(a, b, matematica.calculadora.sumar(a, b)))
print("{} * {} = {}".format(a, b, matematica.calculadora.multiplicar(a, b)))
print("{} - {} = {}".format(a, b, matematica.calculadora.restar(a, b)))
print("{} / {} = {}".format(a, b, matematica.calculadora.dividir(a, b)))